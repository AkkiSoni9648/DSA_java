package BitManipula;

public class bitwise_or_And {
    public static void main(String[] args) {
        System.out.println(5|17);// alaways give biiger value  
        System.out.println(6&21);// always give smaller value than input
        System.out.println(5&5);//Same output on any operator
        System.out.println(5|5);
        System.out.println(~10);
        //Right shift by 2
        System.out.println(91>>2);
        System.out.println(91<<2);//Left Shift by 2
        System.out.println(1<<5);//(2^n=1<<n)
    }
}
